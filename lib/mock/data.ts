// ── Accessors (the UI never touches the arrays directly) ────────
export function getTrips(): Trip[] {
  return TRIPS;
}

export function getTripById(id: string): Trip | undefined {
  return TRIPS.find((t) => t.id === id);
}

export function getMessages(): LineMessage[] {
  return [...MESSAGES].sort(
    (a, b) => new Date(b.sentAt).getTime() - new Date(a.sentAt).getTime(),
  );
}

export function getReviewItems(): ReviewItem[] {
  return REVIEW_ITEMS;
}

export function getMessagesForTrip(tripId: string): LineMessage[] {
  return MESSAGES.filter((m) => m.linkedTripId === tripId).sort(
    (a, b) => new Date(a.sentAt).getTime() - new Date(b.sentAt).getTime(),
  );
}
